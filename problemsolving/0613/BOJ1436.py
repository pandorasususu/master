#BOJ1436영화감독 숌

# 666 X~X
# 1

# X666 1~9
# 666X 0~9
# 19

# XX666 10~99
# X666X 10~99
# 666XX 00~99
# 280

# XXX666 100~999
# XX666X 100~999
# X666XX 100~999
# 666XXX 000~999
# 3700

# N은 10,000 이하의 자연수이므로, 영화제목은 최대 7자리수

N = int(input())
cnt = 0
for i in range(666, 9999999):
    if '666' in str(i):
        cnt += 1
        if cnt == N:
            print(i)
            break




